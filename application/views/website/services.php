<h3>Refund Policy</h3>
At DTS, we aim to provide hassle-free services, but refunds are available under specific conditions:</br>
•	Cancellation by DTS: If a test is canceled by DTS, a full refund will be issued within 10 business days.</br>
•	Technical Issues: If candidates face verified technical issues during payment or registration, refunds will be provided after thorough verification.</br>
•	Non-Eligibility: Fees will not be refunded for disqualification due to incorrect data provided by candidates.</br>
•	No-show Policy: Candidates failing to appear for tests are not eligible for refunds.</br>
Refund requests must be submitted via <a href="info@dts.org.pk">info@dts.org.pk</a> with complete payment details.
